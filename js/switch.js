let day0Week = 'friday';

switch(day0Week) {
    case 'friday':
        console.log('Friday');
        break;
    case'saturday':
        console.log('Saturday');
        break;
    case'sunday':
        console.log('Sunday');
        break;
    case'monday':
        console.log('Monday');
        break;
    case 'tuesday':
        console.log('Tuesday');
        break;
    case 'wednesday':
        console.log('Wednesday');
        break;
    case 'thursday':
        console.log('Thursday');
        break;
    default: 
    console.log('Wrong day');
        break;
}